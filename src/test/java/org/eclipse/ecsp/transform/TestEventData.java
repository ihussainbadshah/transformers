package org.eclipse.ecsp.transform;

import com.fasterxml.jackson.annotation.JsonFilter;
import org.eclipse.ecsp.entities.AbstractEventData;

/**
 * TestEventData class.
 */
@JsonFilter("TestFilter")
public class TestEventData extends AbstractEventData {

    private static final long serialVersionUID = -1147106467326210246L;
    private int retryAttempts;

    public TestEventData(int retryAttempts) {
        this.retryAttempts = retryAttempts;
    }

    public int getRetryAttempts() {
        return retryAttempts;
    }

    public void setRetryAttempts(int retryAttempts) {
        this.retryAttempts = retryAttempts;
    }

}
